export interface LocationOption {
  label: string;
  value: string;
}

export const getStates = (): LocationOption[] => {
  return US_STATES;
};

export const getCitiesByState = (stateCode: string): LocationOption[] => {
  const cities = CITIES_BY_STATE[stateCode] || [];
  // Cities are already sorted alphabetically in the CITIES_BY_STATE object
  return cities.map(city => ({ label: city, value: city }));
};

export const getZipCodesByCity = (stateCode: string, cityName: string): LocationOption[] => {
  const stateCities = ZIP_CODES_BY_CITY[stateCode];
  if (!stateCities) {
    console.log('No cities found for state:', stateCode);
    return [];
  }
  
  const zipCodes = stateCities[cityName];
  if (!zipCodes) {
    console.log('No zip codes found for city:', cityName, 'in state:', stateCode);
    return [];
  }
  
  // Zip codes are already sorted
  return zipCodes.map(zip => ({ label: zip, value: zip }));
};

